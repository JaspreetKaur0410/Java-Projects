    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(txtphone, ecosystem.getCustomerDirectory().getCustomerlist());
        DefaultTableModel model = (DefaultTableModel) tblCustomers.getModel();
        Customer customer = (Customer)tblCustomers.getValueAt(tblCustomers.getSelectedRow(), 0);
        
        
        if(tblCustomers.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(txtphone, "Please Select Customer to Delete");
        }
        else{
            ecosystem.getCustomerDirectory().deleteCustomer(customer);
            ecosystem.getUserAccountDirectory().deleteUser(customer);
            //ecosystem.getUserAccountDirectory().del
            //ecosystem.getUserAccountDirectory()
            JOptionPane.showMessageDialog(txtphone, "CUSTOMER" + " "+txtfullname.getText()+ " " + "DELETED");
        }
        populateTable();
    }//GEN-LAST:event_btn_deleteActionPerformed
    
    public void populateTable(){
        
        CustomerDirectory customerDirectory = ecosystem.getCustomerDirectory();
        DefaultTableModel model = (DefaultTableModel) tblCustomers.getModel();

        model.setRowCount(0);
        for (Customer customer : customerDirectory.getCustomerlist()) {
                    Object[] row = new Object[5];
                    row[0] = customer;
                    row[1] = customer.getCustomerid();
                    row[2] = customer.getUsername();
                    row[3] = customer.getAddress();
                    row[4] = customer.getPhone();

                    model.addRow(row);
                
            }
        
        
    }

