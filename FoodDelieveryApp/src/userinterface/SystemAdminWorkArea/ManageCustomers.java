    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        String digit_regex = "[0-9]+";
        
        if(txtusername.equals("") || txtfullname.equals("") || txtpwd.equals("") || txtphone.equals("")
                || txtaddress.equals("")){
            JOptionPane.showMessageDialog(txtphone, "Please provide full details");
                    
        }
        else if(!txtphone.getText().matches(digit_regex)){
            JOptionPane.showMessageDialog(txtphone, "Please enter correct phone number");
        }
        else{
            JOptionPane.showMessageDialog(txtphone, ecosystem.getUserAccountDirectory().getUserAccountList());
           if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtusername.getText())){
               ecosystem.getUserAccountDirectory().createUserAccount(txtusername.getText(), txtpwd.getText(), new CustomerRole());
               Customer customer = ecosystem.getCustomerDirectory().addCustomer();
               customer.setUsername(txtusername.getText());
               customer.setPassword(txtpwd.getText());
               customer.setAddress(txtaddress.getText());
               customer.setName(txtfullname.getText());
               customer.setPhone(Integer.parseInt(txtphone.getText()));
               JOptionPane.showMessageDialog(txtphone, "CUSTOMER" + " "+txtusername.getText()+ " " + "Added");
               txtusername.setText("");
               txtpwd.setText("");
               txtaddress.setText("");
               txtphone.setText("");
               txtfullname.setText("");
           }
           else{
               JOptionPane.showMessageDialog(txtphone, "Username" + " " + "ALREADY EXIST");
           }
            
        }
        populateTable();
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_modifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifyActionPerformed
        // TODO add your handling code here
        if(tblCustomers.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(txtphone, "Select Customer to modify");
        }
        else{
            Customer customer = (Customer)tblCustomers.getValueAt(tblCustomers.getSelectedRow(), 0);
            btnmodificationsaved.setEnabled(true);
            txtusername.setText(customer.getUsername());
            txtpwd.setText(customer.getPassword());
            txtaddress.setText(customer.getAddress());
            txtphone.setText(String.valueOf(customer.getPhone()));
            txtfullname.setText(customer.getName());
            btn_add.setEnabled(false);
        }
        
    }//GEN-LAST:event_btn_modifyActionPerformed

