    public void populateTable(){
       //Character size_here[];
       DefaultTableModel model = (DefaultTableModel)tbl_menu.getModel();
       model.setRowCount(0);
       for(Restaurant restaurant:system.getRestaurantDirectory().getRestrauntlist()){
            //JOptionPane.showMessageDialog(btn_modify, restaurant.getMenu().getItemlist());
            if(String.valueOf(restaurant.getName()).equals(String.valueOf(userAccount.getUsername()))){
                Object[] row = new Object[3];
                    for(Item item:restaurant.getMenu().getItemlist()){
                        //size_here = new Character[item.getSizearr().length];
                        //JOptionPane.showMessageDialog(btn_modify, item + " " + item.getSizearr());
                        row[0] = item;
                        row[1] = item.getItemprice();
                        for(int i=0;i<item.getSizearr().length;i++){
                            if(item.getSizearr().length == 1){
                                row[2] = item.getSizearr()[0];
                            }
                            else if(item.getSizearr().length == 2){
                                row[2] = item.getSizearr()[0] + "," + item.getSizearr()[1];
                            }
                            else if(item.getSizearr().length == 3){
                                row[2] = item.getSizearr()[0] + "," + item.getSizearr()[1] + "," + item.getSizearr()[2];
                            }
                        }
                        model.addRow(row);
                    }
            }
       }      
    }
    
    
