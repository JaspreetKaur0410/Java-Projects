    private void btnmodificationsavedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificationsavedActionPerformed
        // TODO add your handling code here:
        
        if(btnmodificationsaved.isEnabled()){
            
            Double price = 0.0;
            ArrayList<Character> itemsize = new ArrayList<>();
            Character sizes[];
            HashMap<Character, Integer> sizemap = new HashMap<Character, Integer>();

            int counter = -1;
            int flag = 0;

            if(check_s.isSelected()){
                sizemap.put('s', 1);
            }
            else if(!check_s.isSelected()){
                sizemap.put('s',0);
            }
            if(check_m.isSelected()){
                sizemap.put('m', 1);
            }
            else if(!check_m.isSelected()){
                sizemap.put('m',0);
            }
            if(check_l.isSelected()){
                sizemap.put('l', 1);
            }
            else if(!check_l.isSelected()){
                sizemap.put('l',0);
            }
     
            int selectedrow = tbl_menu.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel)tbl_menu.getModel();
            Item item = (Item)model.getValueAt(selectedrow, 0);
            if(tbl_menu.getSelectedRow() < 0){
                JOptionPane.showMessageDialog(txt_name, "Please Select Customer");
            }
           
            item.setItemname(String.valueOf(txt_name.getText()));
            try{
                price = Double.valueOf(txt_price.getText());
               }
               catch(Exception e){
                   JOptionPane.showMessageDialog(btn_modify, "Please enter correct price");
                   return;
               }
               item.setItemprice(price);
               //size
               for (var entry : sizemap.entrySet()) {
                    if(entry.getValue() == 1){
                        counter++;
                        itemsize.add(entry.getKey());     
                    }
               
               }
               sizes = new Character[itemsize.size()];
               sizes = itemsize.toArray(sizes);
               item.setSizearr(sizes);
               JOptionPane.showMessageDialog(btn_modify, "Item Modified");
            
                txt_name.setText("");
                txt_price.setText("");
                check_l.setSelected(false);
                check_m.setSelected(false);
                check_s.setSelected(false);
                btn_add.setEnabled(true);
            }
            populateTable();
        
    }//GEN-LAST:event_btnmodificationsavedActionPerformed

