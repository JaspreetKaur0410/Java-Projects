    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:
        
        //regex
        String digit_regex = "[0-9]+";
        
        String alergy1 = txt_allergy1.getText();
        String alergy2 = txt_allergy2.getText();
        String alergy3 = txt_allergy3.getText();
        
        if(txt_mrn.getText().equals("") || 
                alergy1.equals("") || alergy2.equals("") || alergy3.equals("")){
            JOptionPane.showMessageDialog(btn_clear, "Please provide full Information");  
        }
        else if(!txt_mrn.getText().matches(digit_regex)){
            JOptionPane.showMessageDialog(btn_clear, "Please enter medical number in digits");
        }
        else{
            personinfo.setMrn(Integer.parseInt(txt_mrn.getText()));
            personinfo.setAlergy1(alergy1);
            personinfo.setAlergy2(alergy2);
            personinfo.setAlergy3(alergy3);
            JOptionPane.showMessageDialog(btn_clear, "Medical Information Added");
        }
        
    }//GEN-LAST:event_btn_insertActionPerformed

