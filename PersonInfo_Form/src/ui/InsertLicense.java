    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:

        //CONVERT LOCAL DATE TO DATE
        LocalDateTime now = LocalDateTime.now();
        ZoneId defaultzoneid = ZoneId.systemDefault();
        ZonedDateTime zdt = now.atZone(defaultzoneid);
        Date to_Date_now = Date.from(zdt.toInstant());

        String licensenumber = String.valueOf(txt_licensenumber.getText());
        Date issued_date = dateissue.getDate();
        Date expire_date = dateexpire.getDate();
        String bloodtype = String.valueOf(combo_bloodtype.getSelectedItem());

        if(licensenumber.equals("") || issued_date.equals("") || expire_date.equals("")
            || combo_bloodtype.getSelectedIndex() == 0 || image.getIcon()==null){
            JOptionPane.showMessageDialog(dateexpire, "Please provide all information");
        }
        else if(issued_date.after(to_Date_now)){
            JOptionPane.showMessageDialog(dateexpire,"Please select correct issue date");
        }
        else if(expire_date.before(to_Date_now)){
            JOptionPane.showMessageDialog(dateexpire,"Please enter coreect expiration date");
        }
        else if(expire_date.before(issued_date)){
            JOptionPane.showMessageDialog(dateexpire,"Expiration Date can not be after Issued Date");
        }

        else{
            personinfo.setLicensenumber(Integer.parseInt(licensenumber));
            personinfo.setIssued(issued_date);
            personinfo.setExpiration(expire_date);
            personinfo.setBloodType(bloodtype);
            JOptionPane.showMessageDialog(dateexpire,"Liceanse Details Added");
        }

    }//GEN-LAST:event_btn_insertActionPerformed

