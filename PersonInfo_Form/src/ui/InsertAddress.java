    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here:
        
        String digit_regex = "[0-9]+";
        String zip_code_regex = "^[0-9]{5}(?:-[0-9]{4})?$";

        String streetaddress = txt_sreetaddress.getText();
        int city_index = combo_city.getSelectedIndex();
        String city = String.valueOf(combo_city.getSelectedItem());
   
        String zipcode = String.valueOf(txt_zipcode.getText());
        
        if(streetaddress.equals("") || zipcode.equals("")){
            JOptionPane.showMessageDialog(combo_city, "Please provide full information");
        }
        
        else if(!zipcode.matches(digit_regex)){
            JOptionPane.showMessageDialog(combo_city,"Please enter correct zipcode");
        }
        else if(!zipcode.matches(zip_code_regex)){
            JOptionPane.showMessageDialog(combo_city, "Please enter 5 or 9 digit zipcode");
        }
        else{
            personalinfo.setStreet_address(streetaddress);
            personalinfo.setCity(city);
            personalinfo.setZipcode(Long.parseLong(zipcode));
            JOptionPane.showMessageDialog(combo_city, "Address Added");
       }
        
        
    }//GEN-LAST:event_btn_insertActionPerformed

    private void combo_cityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_cityActionPerformed
        // TODO add your handling code here:
        
        int city_index = combo_city.getSelectedIndex();
        if(city_index >= 0 && city_index <=8){
            combo_state.setSelectedIndex(0);
            
            }
            else if(city_index >= 9 && city_index<=13){
                combo_state.setSelectedIndex(1);
            }
            else if(city_index >= 14 && city_index<=19){
                combo_state.setSelectedIndex(2);
            }
            personalinfo.setState((String) combo_state.getSelectedItem());
    }//GEN-LAST:event_combo_cityActionPerformed

