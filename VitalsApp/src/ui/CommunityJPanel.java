    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel)tbl_community.getModel();
        model.setRowCount(0);
        for(City c:city.getCitylist()){
            for(Community comm : c.getCommunitylist()){
            Object row[] = new Object[2];
            row[0] = c;
            row[1] = comm;
            model.addRow(row);
            }
        }

    }
