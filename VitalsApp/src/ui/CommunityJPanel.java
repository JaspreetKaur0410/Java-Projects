    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel)tbl_community.getModel();
        int selectedrow = tbl_community.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(btn_remove, "Please select Community");
        }
        else{
            Community c = (Community)tbl_community.getValueAt(selectedrow, 1);
            City ci = (City)tbl_community.getValueAt(selectedrow, 0);

            ci.removeCommunity(c);
            JOptionPane.showMessageDialog(btn_remove, "Community Removed from City " + ci);

            refreshTable();
        }
        
        
    }//GEN-LAST:event_btn_removeActionPerformed
    
    

