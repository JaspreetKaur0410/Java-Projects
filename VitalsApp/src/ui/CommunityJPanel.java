    private void btn_addcommunityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addcommunityActionPerformed
        // TODO add your handling code here:
        String alphabets_regex = "^[a-zA-Z]*$";
  
        int selectedcityindex = combo_city.getSelectedIndex();
        if(selectedcityindex == 0){
            JOptionPane.showMessageDialog(combo_city, "Please Select City");
            return;
        }
        else  if(txt_community.getText().equals("")){
            JOptionPane.showMessageDialog(jScrollPane1, "Please enter community");
        }
        else if(!txt_community.getText().matches(alphabets_regex)){
            JOptionPane.showMessageDialog(jScrollPane1, "Please enter correct community name");
        }
        else{
            boolean check = checkComm();
            if(check == false){
                for(City c:city.getCitylist()){
                    if(c.getName().equals(combo_city.getSelectedItem())){
                        City cityselected = c;
                        Community community = c.addcommunity();
                        community.setName(txt_community.getText());
                        city.getCommunitylist().add(community);
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(combo_city, "Community already exist");
            }
            
        }
    
        refreshTable();
        
    }//GEN-LAST:event_btn_addcommunityActionPerformed
    public boolean checkComm(){
        
        for(City c:city.getCitylist()){
            if(String.valueOf(c.getName()).equals(combo_city.getSelectedItem())){
                for(int i=0;i<c.getCommunitylist().size();i++){
                    
                    if(String.valueOf(c.getCommunitylist().get(i).getName()).
                            equals(String.valueOf(txt_community.getText()))){
                        return true;
                    }
                }
            }
        
    }
        return false;
    }

    
