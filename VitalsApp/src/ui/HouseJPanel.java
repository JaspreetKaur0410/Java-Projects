    private void btn_addhouseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addhouseActionPerformed
        // TODO add your handling code here:
        
        int selectedcityindex = combo_city.getSelectedIndex();
        int selectedcommindex = combo_community.getSelectedIndex();
        String house = txt_house.getText();
         
        if(selectedcityindex == 0){
            JOptionPane.showMessageDialog(combo_city, "Please select city");
        }
        else if(selectedcityindex == 0){
            JOptionPane.showMessageDialog(combo_city, "Please select community");
        }
        else  if(txt_house.getText().equals("")){
            JOptionPane.showMessageDialog(jScrollPane1, "Please enter house");
        }
        
        else{
            boolean check = checkHouse();
            if(check == false){
                for(City c:city.getCitylist()){
                    if(c.getName().equals(combo_city.getSelectedItem())){
                        for(Community comm : city.getCommunitylist()){
                            if(comm.getName().equals(combo_community.getSelectedItem())){
                                h = comm.addHouse();
                                h.setStreetname(house);
                            }
                        }
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(combo_city, "House Already exist");
            }
        }
        
        refreshTable();

    }//GEN-LAST:event_btn_addhouseActionPerformed
    
    public boolean checkHouse(){
    
        for(City c:city.getCitylist()){
            //if(String.valueOf(c.getName()).equals(String.valueOf(combo_city.getSelectedItem()))){
                for(Community comm:c.getCommunitylist()){
                    //if(String.valueOf(comm.getName()).equals(String.valueOf(combo_community.getSelectedItem()))){
                    for(House h:comm.getHouselist()){
                        if(String.valueOf(h).equals(String.valueOf(String.valueOf(txt_house.getText())))){
                            return true;
                        }
                    }
                    //}
                }
            //}
        
        }
        return false;
    
    }
    
