    private void btn_removehouseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removehouseActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tbl_house.getModel();
        int selectedrow = tbl_house.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(btn_removehouse, "Please select Community");
        }
        else{
            Community c = (Community)tbl_house.getValueAt(selectedrow, 1);
            City ci = (City)tbl_house.getValueAt(selectedrow, 0);
            House h = (House)tbl_house.getValueAt(selectedrow, 2);
            
            c.removeHouse(h);
            JOptionPane.showMessageDialog(combo_city, "House Removed from City"+ci+" "+c);
            refreshTable();
        }
        
        
    }//GEN-LAST:event_btn_removehouseActionPerformed


