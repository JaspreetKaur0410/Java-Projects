    private void btn_addcityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addcityActionPerformed
        // TODO add your handling code here:
        String alphabets_regex = "^[a-zA-Z]*$";
        if(txt_city.getText().equals("")){
            JOptionPane.showMessageDialog(jScrollPane1, "Please enter city");
        }
        else if(!txt_city.getText().matches(alphabets_regex)){
            JOptionPane.showMessageDialog(jScrollPane1, "Please enter correct city name");
        }
        
        else{
        boolean check = checkCity();
        if(check == false){
            City c = city.addCity();
            c.setName(String.valueOf(txt_city.getText()));
            //JOptionPane.showMessageDialog(jScrollPane1, "City Added");
            refreshTable();
        }
        else{
            JOptionPane.showMessageDialog(btn_remove, "City already exist");
        }
        }
    }//GEN-LAST:event_btn_addcityActionPerformed
    
    public boolean checkCity(){
        for(int i=0;i<city.getCitylist().size();i++){
            if(String.valueOf(city.getCitylist().get(i)).equals(String.valueOf(txt_city.getText()))){
                return true;
            }
        }
        return false;
    }
    
    
    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel)tbl_city.getModel();
        int selectedrow = tbl_city.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(btn_remove, "Please select City");
        }
        City c = (City)tbl_city.getValueAt(selectedrow, 0);
        city.removeCity(c);
        JOptionPane.showMessageDialog(btn_remove, "City Removed");
        refreshTable();
        
    }//GEN-LAST:event_btn_removeActionPerformed


