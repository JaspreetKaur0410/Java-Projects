    public void refreshTable() {
        int rowCount = tbl_city.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tbl_city.getModel();
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        //JOptionPane.showMessageDialog(jScrollPane1, city.getCitylist());
        for(City c:city.getCitylist()){
            Object row[] = new Object[1];
            //row[0] = c.getCityid();
            row[0]=c;
            model.addRow(row);
        }
    }
