    private void btn_assignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_assignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_mediaction.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a work request");
            return;
        }

        Object receiverval = tbl_mediaction.getValueAt(selectedRow, 3);
        Object statusval = tbl_mediaction.getValueAt(selectedRow, 4);
        Child childval = (Child) tbl_mediaction.getValueAt(selectedRow,5);

        if ("Request for medication".equals(statusval) || receiverval == null) {
            PharmacyWorkRequest pwr = (PharmacyWorkRequest) tbl_mediaction.getValueAt(selectedRow, 6);
            pwr.setReceiver(account);
            pwr.setStatus("Under Medication");
            pwr.setChild(childval);
            pwr.setMessage("Under Medication");

            populateTable();
            JOptionPane.showMessageDialog(null, "Work request is now assigned to you");
            checkStatus(selectedRow);
            populateTable();

        }
        else{
            if (receiverval.equals(account)) {
                JOptionPane.showMessageDialog(null, "Work request is already assigned to you");
            }
            else if (!receiverval.equals(account.getUsername())) {
                JOptionPane.showMessageDialog(null, "Work request is assigned to someone else");
                checkStatus(selectedRow);
            }
        }
    }//GEN-LAST:event_btn_assignActionPerformed

    private void btn_undermedicationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_undermedicationActionPerformed
        // TODO add your handling code here:
        
        //CONVERT LOCAL DATE TO DATE
        LocalDateTime now = LocalDateTime.now();
        ZoneId defaultzoneid = ZoneId.systemDefault();
        ZonedDateTime zdt = now.atZone(defaultzoneid);
        Date to_Date_now = Date.from(zdt.toInstant());
        
        DefaultTableModel model = (DefaultTableModel) tbl_mediaction.getModel();
        int selectedRow = tbl_mediaction.getSelectedRow();
        Child childval = (Child)tbl_mediaction.getValueAt(selectedRow, 5);
        
        PharmacyWorkRequest workRequest = (PharmacyWorkRequest)tbl_mediaction.getValueAt(selectedRow, 6);
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a child from table before proceeding");
            return;
        }
        else{
        for(Child c:business.getChildDirectory().getChildlist()){
            if(childval.equals(c)){
                ChildPrescriptions cp = childval.addChildPrescriptions();
                cp.setPrescription(workRequest.getPrescription());
                cp.setPrescriptiondate(to_Date_now);
                cp.setIsMedicated(true);
            }
        }
        
        for(Child c:childval.getOrg().getChildDirectory().getChildlist()){
            if(childval.equals(c)){
                ChildPrescriptions cp = childval.addChildPrescriptions();
                cp.setPrescription(workRequest.getPrescription());
                cp.setPrescriptiondate(to_Date_now);
                cp.setIsMedicated(true);
            }
        }
        
        for(WorkRequest wr:((WorkRequest)workRequest).getSender().getWorkQueue().getWorkRequestList()){
            wr.setStatus("Medication Provided");
            wr.setMessage("Medication Provided");
            JOptionPane.showMessageDialog(rightpanel1, wr);
        }
        JOptionPane.showMessageDialog(rightpanel1, "Medication Provided");
        }
        populateTable();

    }//GEN-LAST:event_btn_undermedicationActionPerformed


