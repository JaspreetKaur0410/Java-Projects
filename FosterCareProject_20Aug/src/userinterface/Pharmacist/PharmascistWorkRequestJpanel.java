     public void checkStatus(int selectedRow){
        DefaultTableModel model = (DefaultTableModel) tbl_mediaction.getModel();
        Object statusval = tbl_mediaction.getValueAt(selectedRow, 4);
        
        if(statusval.equals("Request for medication")){
            panel_assign.setVisible(true);
            btn_assign.setVisible(true);
            panel_undermedication.setVisible(false);
            btn_undermedication.setVisible(false);
        }
        else if(statusval.equals("Under Medication")){
            panel_assign.setVisible(false);
            btn_assign.setVisible(false);
            panel_undermedication.setVisible(true);
            btn_undermedication.setVisible(true);
        }
        else if(statusval.equals("Medication Provided")){
            panel_assign.setVisible(false);
            btn_assign.setVisible(false);
            panel_undermedication.setVisible(true);
            btn_undermedication.setVisible(true);
        }
     }
     
    
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tbl_mediaction.getModel();
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
             if (request instanceof PharmacyWorkRequest) {
                Object[] row = new Object[model.getColumnCount()];
                row[0] = request.getSender().getUsername();
                row[1] = request.getMessage();
                row[2] = request.getSender();
                row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                //String result = ((MedicalCheckRequest) request).getTestResult();
                row[4] = request.getStatus();
                row[5] = request.getChild();
                row[6] = request;
                model.addRow(row);
            }
        }
    }

