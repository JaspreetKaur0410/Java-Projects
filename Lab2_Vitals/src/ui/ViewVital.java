    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        
        int selected_row = tbl_vital.getSelectedRow();
        
        if(selected_row < 0){
            JOptionPane.showMessageDialog(jScrollPane1, "Please select row to delete");
            return;
        }
        
        else{
            
            DefaultTableModel model = (DefaultTableModel) tbl_vital.getModel();
            
            
            String vs_str = (String) model.getValueAt(selected_row, 0);
            model.removeRow(selected_row);
            vitalhistory.deleteVital(vs_str);
            
            JOptionPane.showMessageDialog(jScrollPane1, vitalhistory.getHistory());
            populateTable();
        }
        
        
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
        // TODO add your handling code here:
        
        int selected_row = tbl_vital.getSelectedRow();
        
        if(selected_row < 0){
            JOptionPane.showMessageDialog(jScrollPane1, "Please select row to view");
            return;
        }
        
        else{
            DefaultTableModel model = (DefaultTableModel) tbl_vital.getModel();
            String vs_str = (String) model.getValueAt(selected_row, 0);
            VitalSigns vs_return = vitalhistory.viewVital(vs_str);
            txt_temp.setText(String.valueOf(vs_return.getTemperature()));
            txt_date.setText(vs_return.getDate());
            txt_pulse.setText(String.valueOf(vs_return.getPulse()));
            txt_bp.setText(String.valueOf(vs_return.getBloodpressure()));
            
        }
        
        
    }//GEN-LAST:event_btn_viewActionPerformed


