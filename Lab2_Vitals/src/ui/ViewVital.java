    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel)tbl_vital.getModel();
        //JOptionPane.showMessageDialog(jScrollPane1, model);
        model.setRowCount(0);
        
        for(VitalSigns vs:vitalhistory.getHistory()){
            Object[] row = new Object[4];
            row[0] = vs.getDate();
            row[1] = vs.getTemperature();
            row[2] = vs.getPulse();
            row[3] = vs.getBloodpressure();
            //txt_hide.setText(vs);
            model.addRow(row);
        }
        
    }
