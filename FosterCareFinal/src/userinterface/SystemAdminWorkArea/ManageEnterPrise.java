    private void btn_addenterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addenterpriseActionPerformed
        // TODO add your handling code here:
        
       for(Network network:system.getNetworkList()){
           if(network.getName().equals(combostate.getSelectedItem())){
               network_g = network;
           }
       }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            if(type.getValue().equals(comboenterprise.getSelectedItem())){
                enterprisetype_g = type;
            }
        }
        if (network_g == null || enterprisetype_g == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        String name = txt_enterprisename.getText();
        if (!name.isEmpty()) {
            if (network_g.getEnterpriseDirectory().isUnique(name)) {
                Enterprise enterprise = network_g.getEnterpriseDirectory().createAndAddEnterprise(name, enterprisetype_g);
                
                JOptionPane.showMessageDialog(null, "Enterprise created! ");
                txt_enterprisename.setText("");
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Enterprise already exists!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Enter enterprise name");
        }
        
    }//GEN-LAST:event_btn_addenterpriseActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) tbl_enterprise.getModel();
        int selectedrow = tbl_enterprise.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(null, "Choose Enterprise");
            return;
        }
        else{
            Enterprise ennt = (Enterprise) model.getValueAt(selectedrow, 2);
            Network network1 = (Network) model.getValueAt(selectedrow, 0);
            for(Network network:system.getNetworkList()){
                if(network.equals(network1)){
                    network.getEnterpriseDirectory().deleteEnterprise(ennt);
                }
            }
            populateTable();
            JOptionPane.showMessageDialog(null, "Enterprise Deleted");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) tbl_enterprise.getModel();
        int selectedrow = tbl_enterprise.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(null, "Choose State");
            return;
        }
        else{
            btn_save.setVisible(true);
            panel_update.setVisible(true);
            Network network = (Network) model.getValueAt(selectedrow, 0);
            Enterprise ennt = (Enterprise) model.getValueAt(selectedrow, 2);
            txt_state.setText(network.getName());
            txt_type.setText(ennt.getEnterpriseType().getValue());
            txt_ebtname.setText(ennt.getName());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

