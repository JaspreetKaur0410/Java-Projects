    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tbl_enterprise.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = network;
                row[1] = enterprise.getEnterpriseType().getValue();
                row[2] = enterprise;
                model.addRow(row);
            }
        }
    }
    private void populateStateEnterprise(){
        combostate.removeAllItems();
        comboenterprise.removeAllItems();
        for(Network network:system.getNetworkList()){
            combostate.addItem(network.getName());
        }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            comboenterprise.addItem(type.getValue());
        }
    }
    
