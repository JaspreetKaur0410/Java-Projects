    public void checkStatus(int selectedRow){
        DefaultTableModel model = (DefaultTableModel) tbl_parentcheckstatus.getModel();
        Object statusval = tbl_parentcheckstatus.getValueAt(selectedRow, 1);
        
        if(statusval.equals("Appointment Scheduled")){
            panel_viewappoitment.setVisible(true);
            btn_viewAppointment.setVisible(true);
            
        }
        else if(statusval.equals("ADOPTED")){
            parent.setFlag(false);
            panel_viewappoitment.setVisible(false);
            btn_viewAppointment.setVisible(false);           
        }
      
        else{          
            panel_viewappoitment.setVisible(false);
            btn_viewAppointment.setVisible(false);
        }
        
    }
    
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tbl_parentcheckstatus.getModel();
        model.setRowCount(0);
        
        for(WorkRequest workrequest:parent.getWorkQueue().getWorkRequestList()){
            if(workrequest instanceof ParentWorkRequest){
               
                    Object[] row = new Object[2];
                    row[0] = ((ParentWorkRequest) workrequest).getChild();
                    if(workrequest.getStatus().equals("APPROVED TO ADOPT")){
                        row[1] = "ADOPTED";
                    }
                    else{
                    row[1] =  workrequest.getStatus();
                    }
                    model.addRow(row);
            }

        }
        
    }

