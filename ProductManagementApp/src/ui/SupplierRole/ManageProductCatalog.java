    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
        // TODO add your handling code here:
        
        int selectdrow = tbl_productcatalog.getSelectedRow();
        if(selectdrow < 0){
            JOptionPane.showMessageDialog(jScrollPane1, "Please select row first");
        }
        
        Product p = (Product)tbl_productcatalog.getValueAt(selectdrow, 0);
        
        ViewProduct createproduct = new ViewProduct(userProcessContainer, p);
        userProcessContainer.add("createproduct",createproduct);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btn_viewActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tbl_productcatalog.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Product s = (Product) tbl_productcatalog.getValueAt(selectedRowIndex, 0);
        supplier.getProductcatalog().removeProduct(s);
        refreshTable();
        
    }//GEN-LAST:event_btn_deleteActionPerformed

