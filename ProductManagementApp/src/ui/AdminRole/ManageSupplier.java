    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed

     int row = tbl_supplier.getSelectedRow();
     if(row<0){
         JOptionPane.showMessageDialog(btnRemove, "Please Selct Row");           
     }
     
     Supplier s = (Supplier) tbl_supplier.getValueAt(row, 0);
     ViewSuppliers vs = new ViewSuppliers(userproceescontainer, s);
     userproceescontainer.add("ViewSupplier", vs);
     CardLayout layout = (CardLayout) userproceescontainer.getLayout();
     layout.next(userproceescontainer);

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int row = tbl_supplier.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(btnRemove, "Please Selct Row");
        }
        
        Supplier s = (Supplier)tbl_supplier.getValueAt(row, 0);
        supplierdirectory.removeSupplier(s);
        refreshTable();
    }//GEN-LAST:event_btnRemoveActionPerformed

