    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        
        String digit_regex = "[0-9]+";
        
        String username = txtusername.getText();
        String password = txtpwd.getText();
        String name = txtfullname.getText();
        int phone = Integer.parseInt(txtphone.getText());
        String address = txtaddress.getText();
        
        if(txtusername.equals("") || txtfullname.equals("") || txtpwd.equals("") || txtphone.equals("")
                || txtaddress.equals("")){
            JOptionPane.showMessageDialog(txtphone, "Please provide full details");
                    
        }
        else if(!txtphone.getText().matches(digit_regex)){
            JOptionPane.showMessageDialog(txtphone, "Please enter correct phone number");
        }
        else{
            if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(username)){
                ecosystem.getUserAccountDirectory().createUserAccount(username, password,  new DeliverManRole());
                
                DeliveryMan delieveryman = ecosystem.getDeliveryManDirectory().createdelieverman();
                //JOptionPane.showMessageDialog(txtphone, delieveryman);
                delieveryman.setUsername(String.valueOf(evt));
               
                delieveryman.setUsername(username);
                delieveryman.setPassword(password);
                delieveryman.setPhone(phone);
                delieveryman.setAddress(address);
                delieveryman.setName(name);
                ecosystem.getDeliveryManDirectory().getDelmanlist().add(delieveryman);
                JOptionPane.showMessageDialog(txtphone, "Delievery Person Added!");
                
                txtusername.setText("");
                txtpwd.setText("");
                txtaddress.setText("");
                txtphone.setText("");
                txtfullname.setText("");
            }
            
            else{
               JOptionPane.showMessageDialog(txtphone, "USername" + " " + "ALREADY EXIST");
           }
        }
         populateTable();
    }//GEN-LAST:event_btn_addActionPerformed

