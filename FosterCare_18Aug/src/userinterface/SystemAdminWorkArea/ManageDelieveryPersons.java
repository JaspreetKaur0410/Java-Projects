    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) tbl_delPerson.getModel();
        DeliveryMan delman = (DeliveryMan)tbl_delPerson.getValueAt(tbl_delPerson.getSelectedRow(), 0);
        UserAccount useraccout = (UserAccount)tbl_delPerson.getValueAt(tbl_delPerson.getSelectedRow(), 0);
        
        if(tbl_delPerson.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(txtphone, "Please Select Delievery Person to Delete");
        }
        else{
            ecosystem.getDeliveryManDirectory().deleteDelieveryMan(delman);
            ecosystem.getUserAccountDirectory().deleteUser(useraccout);
            //ecosystem.getUserAccountDirectory()
            JOptionPane.showMessageDialog(txtphone, "Delievery Person DELETED");
        }
        populateTable();
        
    }//GEN-LAST:event_jButton2ActionPerformed

    public void populateTable(){
        DeliveryManDirectory delDir = ecosystem.getDeliveryManDirectory();
        DefaultTableModel model = (DefaultTableModel) tbl_delPerson.getModel();

        model.setRowCount(0);
        for (DeliveryMan delman : delDir.getDelmanlist()) {
                    Object[] row = new Object[5];
                    row[0] = delman;
                    row[1] = delman.getDelievrymanid();
                    row[2] = delman.getUsername();
                    row[3] = delman.getPhone();
                    row[4] = delman.getAddress();

                    model.addRow(row);
                
            }
    }
