    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // TODO add your handling code here:
        if(workRequest.getCustomerfeedback() == null){
        if(r11.isSelected()){
            workRequest.setCustomerfeedback("It was delicious!!");
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Submitted");
        }
        if(r22.isSelected()){
            workRequest.setCustomerfeedback("It was nice");
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Submitted");
        }
        if(r33.isSelected()){
            workRequest.setCustomerfeedback("It was ok ok");
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Submitted");
        }
        if(r44.isSelected()){
            workRequest.setCustomerfeedback("It was not so good");
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Submitted");
        }
        if(r55.isSelected()){
            workRequest.setCustomerfeedback("Very Bad Experience");
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Submitted");
        }
        }
        else{
            JOptionPane.showMessageDialog(feedback_panel, "Feedback Already Sent");           
        }
    }//GEN-LAST:event_jButton1ActionPerformed

