    public void populateOrderTable(){
        Double total = 0.0;
        DefaultTableModel model = (DefaultTableModel)tbl_order.getModel();
        model.setRowCount(0);
        for(OrderItems oi:order.getOrderitemlist()){
            Object[] row = new Object[5];
            row[0] = oi.getItem().getItemname();
            row[1] = oi.getItem().getItemprice();
            row[2] = oi.getSize();
            row[3] = oi.getQuantity();
            row[4] = oi.getItem().getItemprice() * oi.getQuantity();
            model.addRow(row);
            total += oi.getItem().getItemprice() * oi.getQuantity();
        }
        txt_total.setText(String.valueOf(total));
    }
