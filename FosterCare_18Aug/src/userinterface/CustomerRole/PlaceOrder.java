    public boolean createOrder(){
        if(order != null){
            //JOptionPane.showMessageDialog(combo_rests, order.getId());
            order.setRequestDate(new Date());
            order.setStatus("Order Placed");
            order.setMessage(String.valueOf(txt_customermessage.getText()));
            system.getWorkQueue().addWorkRequest(order);
            
            return true;
        }
        else{
            JOptionPane.showMessageDialog(combo_rests, "Please add items to cart");
        }
        return false;
    }
    
    
    
